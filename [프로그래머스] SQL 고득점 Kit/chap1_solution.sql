SELECT *
  FROM ANIMAL_INS;

-- #2
SELECT NAME
     , DATETIME
  FROM ANIMAL_INS
 ORDER BY ANIMAL_ID DESC;


-- #3
SELECT ANIMAL_ID
    , NAME
  FROM ANIMAL_INS
 WHERE INTAKE_CONDITION = 'Sick';

-- #4
SELECT ANIMAL_ID
    , NAME
  FROM ANIMAL_INS
 WHERE INTAKE_CONDITION != 'Aged';

-- #5
SELECT ANIMAL_ID
     , NAME
  FROM ANIMAL_INS
 ORDER BY ANIMAL_ID;

-- #6
SELECT ANIMAL_ID
     , NAME
     , DATETIME
  FROM ANIMAL_INS
 ORDER BY NAME, DATETIME DESC;

-- #7
