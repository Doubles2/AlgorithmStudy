WITH BASE AS (
    SELECT
        MANAGERID,
        COUNT(*) AS CNT
      FROM EMPLOYEE
     GROUP BY MANAGERID
)
SELECT
    E.NAME
  FROM EMPLOYEE E
  JOIN BASE B
    ON E.ID = B.MANAGERID
   AND B.CNT >= 5