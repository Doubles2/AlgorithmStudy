WITH BASE_PRODUCTS_ORDER_2020_FEB AS (
    SELECT 
        P.PRODUCT_ID,
        P.PRODUCT_NAME,
        O.UNIT
      FROM PRODUCTS P
      LEFT JOIN ORDERS O
        ON P.PRODUCT_ID = O.PRODUCT_ID
       AND O.ORDER_DATE BETWEEN DATE('2020-02-01') AND DATE('2020-02-29')
)
SELECT
    PRODUCT_NAME,
    SUM(UNIT) AS UNIT
  FROM BASE_PRODUCTS_ORDER_2020_FEB
 GROUP BY PRODUCT_NAME
 HAVING SUM(UNIT) >= 100