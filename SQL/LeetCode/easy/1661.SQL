WITH BASE AS (
    SELECT
        MACHINE_ID,
        PROCESS_ID,
        MAX(CASE WHEN ACTIVITY_TYPE = 'end' THEN TIMESTAMP ELSE NULL END) 
        - MAX(CASE WHEN ACTIVITY_TYPE = 'start' THEN TIMESTAMP ELSE NULL END) AS processing_time 
      FROM ACTIVITY
     GROUP BY 1, 2
)
SELECT
    MACHINE_ID,
    ROUND(AVG(PROCESSING_TIME), 3) AS PROCESSING_TIME
  FROM BASE
 GROUP BY MACHINE_ID