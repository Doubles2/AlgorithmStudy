WITH BASE_LEAVE_MANAGER AS (
    SELECT
        E1.MANAGER_ID,
        CASE WHEN E2.EMPLOYEE_ID IS NULL THEN 'Y'
             ELSE 'N'
        END AS LEAVE_YN
      FROM EMPLOYEES E1
      LEFT
      JOIN EMPLOYEES E2
        ON E1.MANAGER_ID = E2.EMPLOYEE_ID
     WHERE E1.MANAGER_ID IS NOT NULL
)
SELECT
    DISTINCT E.EMPLOYEE_ID
  FROM EMPLOYEES E
  JOIN BASE_LEAVE_MANAGER B
    ON E.MANAGER_ID = B.MANAGER_ID
   AND B.LEAVE_YN = 'Y'
 WHERE E.SALARY < 30000
 ORDER BY E.EMPLOYEE_ID