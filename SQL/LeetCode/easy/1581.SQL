WITH VISIT_TRANSACTION_BASE AS (
    SELECT
        V.VISIT_ID,
        V.CUSTOMER_ID,
        T.TRANSACTION_ID
      FROM VISITS V
      LEFT JOIN TRANSACTIONS T
        ON V.VISIT_ID = T.VISIT_ID
)
SELECT
    CUSTOMER_ID,
    COUNT(VISIT_ID) AS COUNT_NO_TRANS
  FROM VISIT_TRANSACTION_BASE
 WHERE TRANSACTION_ID IS NULL
 GROUP BY CUSTOMER_ID